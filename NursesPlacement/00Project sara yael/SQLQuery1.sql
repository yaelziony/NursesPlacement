
create database Nurses_Placement

create table Nurse
(
N_id varchar(9) primary key NOT NULL,
N_password varchar(5) NOT NULL,
N_firstName nvarchar(50) ,
N_lastName nvarchar(50) ,
N_tel varchar(10) NOT NULL,
N_email varchar(40),
N_status nvarchar(20)
)

create table Department
(
D_id int primary key identity(10,10),
D_name nvarchar(50),
D_countNursesInShift int ,
D_countNursesInDep int
)

create table NursesInDepartment
(
ND_id_NinD int primary key identity(1,1),
ND_D_id int foreign key references Department(D_id),
ND_N_id varchar(9) foreign key references Nurse(N_id)
)
create table Placement
(
P_id int primary key identity(100,1),
P_idNInD int foreign key  references NursesInDepartment(ND_id_NinD),
P_day int,
P_shift varchar(9)-- משמרת
)

